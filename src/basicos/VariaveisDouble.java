/*
Crie duas variáveis double, calcule a média e imprima.
 */

package basicos;

public class VariaveisDouble {
    public static void main(String[] args) {
        double a = 3.5;
        double b = 7.4;

        double media = (a + b) / 2;
        System.out.println("A média é: " + media);


    }

}
